# Primary Data Types
# Integers, Stings, Float, None, Boolean

#Integer Data Type
int_value = 9899222800
print(type(int_value))
print("Value of Integer DataType:",int_value)

#Strings Data Type
strings_value = "Arbaz Ansari"
print(type(strings_value))
print("Value of String DataType:",strings_value)

#Float Data Type
float_value = 10.555
print(type(float_value))
print("Value of Float DataType:",float_value)

#Boolean Data Type
boolean_true = 1
boolean_false = 0
print(type(boolean_true))
print(type(boolean_false))
print("Value of Boolean DataType:",boolean_true)
print("Value of Boolean DataType:",boolean_false)

happy = None
print(type(happy))
print("Value of None DataType:",happy)

